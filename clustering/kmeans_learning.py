import logging
import os
import sys

import numpy as np
from sklearn.cluster import MiniBatchKMeans
from clustering.data_utils import get_loader

import joblib


logging.basicConfig(
    format="%(asctime)s | %(levelname)s | %(name)s | %(message)s",
    datefmt="%Y-%m-%d %H:%M:%S",
    level=os.environ.get("LOGLEVEL", "INFO").upper(),
    stream=sys.stdout,
)
logger = logging.getLogger("learn_kmeans")


def get_km_model(
    n_clusters,
    init,
    max_iter,
    batch_size,
    tol,
    max_no_improvement,
    n_init,
    reassignment_ratio,
):
    return MiniBatchKMeans(
        n_clusters=n_clusters,
        init=init,
        max_iter=max_iter,
        batch_size=batch_size,
        verbose=1,
        compute_labels=False,
        tol=tol,
        max_no_improvement=max_no_improvement,
        init_size=None,
        n_init=n_init,
        reassignment_ratio=reassignment_ratio,
    )


def load_data(args):
    training_loader = get_loader(args)
    res = []
    for idx, batch_data in enumerate(training_loader):
        if isinstance(batch_data, list):
            data, _ = batch_data
        else:
            data, _ = batch_data["image"], batch_data["label"]
        res.extend(data)

    return res


def learn_kmeans(args):
    np.random.seed(args.seed)
    feat = load_data(args)
    km_model = get_km_model(
        args.n_clusters,
        args.init,
        args.max_iter,
        args.batch_size,
        args.tol,
        args.max_no_improvement,
        args.n_init,
        args.reassignment_ratio,
    )
    km_model.fit(feat)
    joblib.dump(km_model, args.km_path)

    inertia = -km_model.score(feat) / len(feat)
    logger.info("total intertia: %.5f", inertia)
    logger.info("finished successfully")


